public class ConversorNumeros {

    // Converte uma String para Integer.
    public Integer stringToInt(String numero){
        int conversor = Integer.parseInt(numero); // Converte a String para int
        return (conversor); // Retorna o valor convertido
    }

    // Converte uma String para Double.
    public Double stringToDouble(String numero){
        double conversor = Double.parseDouble(numero); // Converte a String para double
        return (conversor); // Retorna o valor convertido
    }
}
